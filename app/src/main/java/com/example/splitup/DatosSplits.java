package com.example.splitup;

public class DatosSplits {
    private String nombre;
    private int id;

    public DatosSplits(String nombre, int id) {
        this.nombre = nombre;
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public int getId() {
        return id;
    }
}
